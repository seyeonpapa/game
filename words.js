// 역방향 두음법칙 맵 (검색용)
const REVERSE_DUEUM = {};
Object.entries(DUEUM_MAP).forEach(([from, to]) => {
  if (!REVERSE_DUEUM[to]) REVERSE_DUEUM[to] = [];
  REVERSE_DUEUM[to].push(from);
});

// 단어 데이터베이스 초기화
function initWordDB() {
  const db = {}; // key: 첫 글자, value: [{word, meaning, level}]
  const allWords = new Set();

  RAW_WORDS.split('\n').forEach(line => {
    line = line.trim();
    if (!line) return;
    const [word, meaning, levelStr] = line.split('|');
    if (!word || !meaning || !levelStr) return;

    const firstChar = word[0];
    const level = parseInt(levelStr);

    if (!db[firstChar]) db[firstChar] = [];
    db[firstChar].push({ word, meaning, level });
    allWords.add(word);
  });

  return { db, allWords };
}

// 두음법칙 적용: 끝 글자에서 시작 가능한 글자들 반환
function getStartChars(lastChar) {
  const chars = [lastChar];
  // 두음법칙 적용
  if (DUEUM_MAP[lastChar]) {
    chars.push(DUEUM_MAP[lastChar]);
  }
  // 역두음법칙도 확인
  if (REVERSE_DUEUM[lastChar]) {
    chars.push(...REVERSE_DUEUM[lastChar]);
  }
  return [...new Set(chars)];
}

// 특정 글자로 시작하는 단어 찾기
function findWords(db, lastChar, maxLevel = 4, usedWords = new Set()) {
  const startChars = getStartChars(lastChar);
  const results = [];

  startChars.forEach(ch => {
    if (db[ch]) {
      db[ch].forEach(entry => {
        if (entry.level <= maxLevel && !usedWords.has(entry.word) && entry.word.length >= 2) {
          results.push(entry);
        }
      });
    }
  });

  return results;
}
